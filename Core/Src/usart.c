/* 작속뵨叫駱 --------------------------------------------------*/
static uint8_t checksum(const uint8_t *buf, uint16_t len)
{
    uint8_t sum = 0;
    for (uint16_t i = 0; i < len; i++) sum += buf[i];
    return sum;
}

/* 瞳 HAL_UART_RxCpltCallback 쟁딧痰 --------------------------*/
void usart1_parse_frame(void)
{
    uint8_t *buf = Usart1type.UsartDMARecBuffer;
    uint16_t len = Usart1type.UsartDMARecLen;
    Usart1type.frame_ok = 0;               // 칵훰呵겨

    if (len < 25) return;                      // 낀똑꼇뭘

    /* 冷관庫 0xA5 */
    while (head < len && buf[head] != 0xA5) ++head;
    if (head + 25 > len) return;               // 假岱낀똑꼇璃寧煉

    /* 叫駱관棺 */
    if (buf[head + 24] != 0x5A) return;

    /* 叫駱뵨 */
    if (checksum(&buf[head + 1], 22) != buf[head + 23]) return;

	memcpy(&Usart1type.raw[0], &buf[head + 1], 23);
	
    Usart1type.frame_ok = 1;
}
