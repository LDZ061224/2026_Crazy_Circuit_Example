/*************************************************
Function: OLED_Cmd
Description:IIC写命令
Details：写命令首先传输（0x78）8位地址码，然后传输
        （0x00）8位命令信号，然后传输OLED_Cmd8位命
         令，具体开始、应答、结束信号产生时刻见如
         下程序
*************************************************/
void OLED_Cmd(unsigned char OLED_Cmd)
{
    I2c_Start();
    I2c_Write_OneByte(0x78);                            //OLED从器件地址
    I2C_Wait_Ask();
    I2c_Write_OneByte(0x00);                            //0x00=0000 0000
    I2C_Wait_Ask();
    I2c_Write_OneByte(OLED_Cmd);
    I2C_Wait_Ask();
    I2c_Stop();
}
/*************************************************
Function: OLED_Dat
Description:IIC写数据
Details：写命令首先传输（0x78）8位地址码，然后传输
        （0x40）8位数据信号，然后传输OLED_Cmd8位命
         令，具体开始、应答、结束信号产生时刻见如
         下程序
*************************************************/
void OLED_Dat(unsigned char OLED_Cmd)
{
    I2c_Start();
    I2c_Write_OneByte(0x78);                            //OLED从器件地址
    I2C_Wait_Ask();
    I2c_Write_OneByte(0x40);                            //器件内单元地址  0x40=0100 0000
    I2C_Wait_Ask();
    I2c_Write_OneByte(OLED_Cmd);                        //将数据写入指定存储单元
    I2C_Wait_Ask();
    I2c_Stop();
}
