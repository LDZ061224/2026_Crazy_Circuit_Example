/*************************************************
Function: OLED_SetPos
Description:定位一个坐标点
Details：定位一个坐标点作为显示一个字符最左上方像素
         点的坐标，x为横坐标（0-127）,y由上至下为纵
         坐标（0-7），每一纵坐标对应8个纵向分辨率。
*************************************************/
void OLED_SetPos(unsigned char x,unsigned char y)
{
    OLED_Cmd(0xb0+y);
    OLED_Cmd(((x&0xf0)>>4)|0x10);
    OLED_Cmd((x&0x0f)|0x01);
}
/*************************************************
Function: OLED_Fill
Description:填充数据
Detail:将整个OLED屏上的128*64个像素点填满fill_Data
       这个数据
*************************************************/
void OLED_Fill(unsigned char fill_Data)
{
    unsigned char m,n;
    for(m=0;m<8;m++)
    {
        OLED_Cmd(0xb0+m);
        OLED_Cmd(0x00);
        OLED_Cmd(0x10);
        for(n=0;n<128;n++)
        {
            OLED_Dat(fill_Data);
        }
    }
}
/**************************************************
Function: OLED_ONE_Number
Description:OLED显示一个数字
Details:x、y为坐标，num为数字
**************************************************/
void OLED_ONE_Number(unsigned char x,unsigned char y,char num)
{
    num+=16;//为跳过前15行字符显示

    OLED_SetPos(x,y);
    int i = 0;
    for(i=0;i<8;i++)
        OLED_Dat(F8X16[num*16+i]);
    OLED_SetPos(x,y+1);

    for(i=0;i<8;i++)
        OLED_Dat(F8X16[num*16+i+8]);
}
/**************************************************
Function: OLED_Numbers
Description:OLED显示一串数字
Details:x、y为坐标，Value为数字
**************************************************/
void OLED_Numbers(unsigned char x,unsigned char y,int Value)
{
    char c=0,j=0;
    int LED_Num[4]={0};

    if(Value>=0)                                //正数
    {
        LED_Num[0] = (Value % 10000) / 1000;    //千位
        LED_Num[1] = (Value % 1000) / 100;       //百位
        LED_Num[2] = (Value % 100) / 10;             //十位
        LED_Num[3] =  Value % 10;                  //个位
    }

    for(c=0,j=0;j<4;x+=8,j++)
    {
        c = LED_Num[j];                          //数字转换
        if(x>120)                             //转到下一行
        {
            x=0;
            y++;
        }

        OLED_ONE_Number(x,y,c);
    }
}
/**************************************************
Function: OLED_ShowStr
Description:OLED显示一串字符
Details:x、y为坐标（x为行，y为列），ch[]为字符，TextSize为字符尺寸
        仅使用2号即8X16尺寸。
**************************************************/
void OLED_ShowStr(unsigned char x,unsigned char y,unsigned char ch[], unsigned char TextSize)
{
    unsigned char c=0,i=0,j=0;
    switch(TextSize)
    {
        case 1:
        {
            while(ch[j]!='\0')
            {
                c = ch[j] - 32;
                if(x > 126)
                {
                    x = 0;
                    y++;
                }

                OLED_SetPos(x,y);

                for(i=0;i<6;i++)
                OLED_Dat(F6x8[c][i]);
                x += 6;
                j++;
            }
        }
        break;
        case 2:
        {
            while(ch[j] != '\0')
            {
                c = ch[j] - 32;//转换到F8X16[]中对应的行数
                if(x > 120) //oled横行只有128个像素点，而一个字符在F8X16[]中需要8个像素点的宽度128-8=120
                {
                    x = 0;
                    y++;
                }
                OLED_SetPos(x,y);//给定坐标（上半部分）
                for(i=0;i<8;i++)//先显示上半部分
                OLED_Dat(F8X16[c*16+i]);
                OLED_SetPos(x,y+1);//给定坐标（下半部分）
                for(i=0;i<8;i++)//再显示下半部分
                OLED_Dat(F8X16[c*16+i+8]);
                x += 8;
                j++;
            }
    }
    break;
    }
}

